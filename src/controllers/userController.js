// Create new user with email verification
exports.createUser = async (req, res) => {
  try {
    const { email } = req.body;
    
    // Check if user already exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({ message: 'Email already registered' });
    }

    // Create new user
    const user = new User(req.body);
    
    // Generate verification token
    const verificationToken = user.generateEmailVerificationToken();
    
    // Save user
    await user.save();
    
    console.log(`ðŸ‘¤ User registered: ${user.name} (${user.email}), ID: ${user._id}`);

    // Send verification email - handle the case if email sending is disabled in development
    let emailSent = false;
    try {
      emailSent = await sendEmail(user.email, 'verifyEmail', verificationToken);
      if (emailSent) {
        console.log(`ðŸ“§ Verification email sent to ${user.email}`);
      } else {
        console.warn(`âš ï¸ Verification email not sent to ${user.email} - email service might not be configured`);
      }
    } catch (emailError) {
      console.error('âŒ Email sending error:', emailError);
      // Continue even if email fails
    }

    res.status(201).json({ 
      message: emailSent 
        ? 'Registration successful. Please check your email to verify your account.'
        : 'Registration successful. Email verification could not be sent.',
      userId: user._id 
    });
  } catch (error) {
    console.error('âŒ User registration error:', error);
    res.status(400).json({ message: error.message });
  }
};

// Verify email
exports.verifyEmail = async (req, res) => {
  try {
    const hashedToken = crypto
      .createHash('sha256')
      .update(req.params.token)
      .digest('hex');

    const user = await User.findOne({
      emailVerificationToken: hashedToken,
      emailVerificationExpires: { $gt: Date.now() }
    });

    if (!user) {
      return res.status(400).json({ message: 'Invalid or expired verification token' });
    }

    // Update user
    user.isEmailVerified = true;
    user.emailVerificationToken = undefined;
    user.emailVerificationExpires = undefined;
    await user.save();

    res.json({ message: 'Email verified successfully' });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

